package TextFile.cw;

public class CompanyApp {
    public static void main(String[] args) {
        String[] tab = new String[3];
        tab[0] = "A";
        tab[1] = "B";
        tab[2] = "C";

        System.out.println(tab.length);
        for (int i = 0; i < tab.length; i++) {
            System.out.println(tab[i]);
        }

    }
}
